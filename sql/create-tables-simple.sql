-- 薄膜出库记录表
CREATE TABLE IF NOT EXISTS film_stock_out (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    out_no VARCHAR(50) NOT NULL,
    stock_id BIGINT NOT NULL,
    detail_id BIGINT NOT NULL,
    material_code VARCHAR(50) NOT NULL,
    batch_no VARCHAR(50) NOT NULL,
    roll_no VARCHAR(50),
    out_area DECIMAL(10,2) NOT NULL,
    out_date DATETIME NOT NULL,
    out_type VARCHAR(20) DEFAULT 'production',
    schedule_id BIGINT,
    coating_task_id BIGINT,
    operator VARCHAR(50),
    receiver VARCHAR(50),
    department VARCHAR(50),
    remark VARCHAR(500),
    create_by VARCHAR(50),
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_out_no (out_no),
    INDEX idx_stock_id (stock_id),
    INDEX idx_detail_id (detail_id),
    INDEX idx_schedule_id (schedule_id),
    INDEX idx_coating_task_id (coating_task_id)
);

